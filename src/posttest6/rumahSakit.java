    private void SimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SimpanActionPerformed
        // TODO add your handling code here:
                
        if (txtNama.getText().length()!=0 && txtTL.getText().length()!=0&&txtUsia.getText().length()!=0&&txtTglMasuk.getText().length()!=0) {
            String nama = txtNama.getText();
            String tgllahir =  txtTL.getText();
            String usiaku=txtUsia.getText();
            int usia = Integer.parseInt(usiaku);
             String jk = null; 
          if (jRadioButton1.isSelected()){ 
            jk = jRadioButton1.getText();
          } else if (jRadioButton2.isSelected()) {
             jk = jRadioButton2.getText();
         }
          String diagnosa= jLDiagnosa.getSelectedValue();
          String dokter=cmbDokter.getSelectedItem().toString();
          String ruangan=cmbRuangan.getSelectedItem().toString();
           String tglmasuk=txtTglMasuk.getText();
            
            if(validasi(nama)){
                JOptionPane.showMessageDialog(this, "Nama Sudah ada ");
            }
            else{
                TambahData(nama,tgllahir,usia,jk,diagnosa,dokter,ruangan,tglmasuk);
            InitTable();
            TampilData();
            JOptionPane.showMessageDialog(this, "Berhasil Simpan Data");
            }
        } 
        else {
        JOptionPane.showMessageDialog(this, "Isi Data Terlebih Dahulu ");    
        
        }
    }//GEN-LAST:event_SimpanActionPerformed

    private void txtCariKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyTyped
        // TODO add your handling code here:
         model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        String cari = comboCari.getSelectedItem().toString();
        try {
            String sql="SELECT  *  from rumahsakit where "+cari+" like '%"+txtCari.getText()+"%'";
           stt = con. createStatement();
            rss = stt. executeQuery(sql);
          ResultSet rss=stt.executeQuery(sql);
            while (rss.next()) {
                Object[] o=new Object[8];
            o[0] = rss.getString("nama_pasien");
            o[1]= rss.getDate("tl");
            o[2] = rss.getInt("usia");
            o[3] = rss.getString("jk");
            o[4]= rss.getString("diagnosa");
            o[5] = rss.getString("dokter");
            o[6] = rss.getString("ruangan");
            o[7]= rss.getDate("tgl_masuk");
                
                model.addRow(o);
            }
            stt.close();
            rss.close();
        }catch(SQLException e) {
            System.out.println("Terjadi kesalahan");
        }
    }//GEN-LAST:event_txtCariKeyTyped

