         private void TampilData(){
        try{
            String sql = "SELECT *  FROM rumahsakit";
            stt = con. createStatement();
            rss = stt. executeQuery(sql);
            while (rss.next()) {
            Object[] o = new Object[8];
            o[0] = rss.getString("nama_pasien");
            o[1]= rss.getDate("tl");
            o[2] = rss.getInt("usia");
            o[3] = rss.getString("jk");
            o[4]= rss.getString("diagnosa");
            o[5] = rss.getString("dokter");
            o[6] = rss.getString("ruangan");
            o[7]= rss.getDate("tgl_masuk");

            
            model.addRow(o);
                
            }
        }catch(SQLException e){
            System.out.println(e.getMessage());
        }
    }
         
         private boolean validasi(String nama){
        try {
            stt = con.createStatement();
            String sql = "SELECT * FROM rumahsakit WHERE nama_pasien='"+nama+"'";
            rss = stt.executeQuery(sql);
            if(rss.next())
                return true;
            else 
                return false;
        } catch (SQLException e) {
            System.out.print(e.getMessage());
            return false;
        }
    }
         
